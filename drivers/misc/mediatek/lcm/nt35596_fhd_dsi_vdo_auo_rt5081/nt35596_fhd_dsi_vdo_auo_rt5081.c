static int RT5081_REG_MASK(kal_uint8 addr, kal_uint8 val, kal_uint8 mask)
{
	kal_uint8 RT5081_reg = 0;
	kal_uint32 ret = 0;

	ret = RT5081_read_byte(addr, &RT5081_reg);

	RT5081_reg &= ~mask;
	RT5081_reg |= val;

	ret = RT5081_write_byte(addr, RT5081_reg);

	return ret;
}

